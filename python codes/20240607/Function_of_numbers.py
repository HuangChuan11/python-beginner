# abs() absolute value
# 絕對值 ｜x｜
print(abs(-5))

# pow() power
print(pow(2, 10))  # 2的10次方 = 1024
print(pow(3, 2))  # 3的2次方 = 9

# min() max() 最大最小值
print(min(4, 2, 1, -10, 0, -1))
print(max(4, 2, 1, -10, 0, -1, 400))

# round()
# 原則上四捨五入
print(round(1.8))
print(round(2.1))
# 但若 x.5 = 奇數，則無條件捨去
print(round(2.5))
print(round(3.5))
